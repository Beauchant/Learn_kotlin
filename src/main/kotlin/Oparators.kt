fun main(){
    val x = 9
    val y = 4.0

    val rsAdd = x + y
    val rsSub = x - y
    val rsMult = x * y
    val rsDiv = x / y
    val rsMod = x % y

    println("$x + $y = $rsAdd")
    println("$x - $y = $rsSub")
    println("$x * $y = $rsMult")
    println("$x / $y = $rsDiv")
    println("$x % $y = $rsMod")
}
